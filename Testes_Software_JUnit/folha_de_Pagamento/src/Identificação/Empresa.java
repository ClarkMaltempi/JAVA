package Identificação;

public class Empresa {
	private String nome_Empresa;
	private String seção;
	
	public void set_nome_Empresa(String nome_Empresa) {
		this.nome_Empresa = nome_Empresa;
	}
	
	public void set_seção(String seção) {
		this.seção = seção;
	}
	
	public String get_nome_Empresa() {
		return nome_Empresa;
	}
	
	public String get_seção() {
		return seção;
	}

}
